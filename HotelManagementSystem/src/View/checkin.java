    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        TableModel model = jTable1.getModel();
        String roomNo = jComboBox1.getSelectedItem().toString();
        String nameTest = jTextField2.getText();
        String checkinDate = jTextField3.getText();
        String checkinTime = jTextField8.getText();
        int peoplePerRoom = Integer.parseInt(jTextField5.getText());
        bookingDAO bd = new bookingDAO();
        int idBooking = bd.searchBooking(roomNo, Date.valueOf(checkinDate)).getIdBooking();
        checkIN cin = new checkIN();
        customer cus = new customer();
        detailBooking db = new detailBooking();
        customerDAO cd = new customerDAO();
        
        if(nameTest.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Every Field is Required");
        }
        else
        {
            cin.setCheckinDate(LocalDate.parse(checkinDate));
            cin.setCheckinTime(checkinTime);
            cin.setIdBooking(idBooking);
            if(cd.addCheckin(cin))
            {
                db.setIdBooking(idBooking);
                if(bd.updateBookingDetail(db))
                {
                    JOptionPane.showMessageDialog(this, "Add To Checkin Successfully!");
                }
                
            }
            
            int idCheckin = cd.searchCheckin(idBooking).getIdCheckin();
            for(int i = 0; i < peoplePerRoom; i++)
            {
                cus.setIdBooking(idBooking);
                cus.setIdCheckin(idCheckin);
                cus.setName(model.getValueAt(i, 0).toString());
                cus.setEmail(model.getValueAt(i, 4).toString());
                cus.setNationality(model.getValueAt(i, 1).toString());
                cus.setMobilePhone(model.getValueAt(i, 3).toString());
                cus.setGender(model.getValueAt(i, 2).toString());
                cus.setIdProof(model.getValueAt(i, 5).toString());
                cus.setAddress(model.getValueAt(i, 6).toString());
                cus.setRoomNo(roomNo);
                if(cd.addCustomer(cus))
                {
                    //JOptionPane.showMessageDialog(this, "Checkin Successfully!");
                    if(i == 0)
                    {
                        JOptionPane.showMessageDialog(this, "Checkin Successfully!");
                    }
                }
                if(i == peoplePerRoom - 1)
                {
                    setVisible(false);
                    new checkin().setVisible(true);
                }
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

