    public void bookingDetails()
    {
        String roomNo = (String)jComboBox1.getSelectedItem();
        String checkinDate = jTextField3.getText();
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        bookingDAO bd = new bookingDAO();
        roomDAO rd = new roomDAO();
        booking b = new booking();
        room rm = new room();
        rm = rd.searchRoom(roomNo);
        b = bd.searchBooking(roomNo, Date.valueOf(checkinDate));
        model.setNumRows(rm.getPeoplePerRoom());
        
        jTextField9.setText(b.getBookingDate().toString());
        jTextField4.setText(b.getDetailbooking().getCheckoutDate().toString());
        jTextField5.setText(String.valueOf(rm.getPeoplePerRoom()));
        jTextField10.setText(rm.getRoomType());
        jTextField7.setText(String.valueOf(rm.getPricePerDay()));
        
            
        if(b.getGuestEmail().equals("") && b.getGuestName().equals(""))
        {
            model.insertRow(1, new Object[]{b.getName(), b.getNationality(), "", b.getMobilePhone(), b.getEmail(), "", ""});
            
        }
        else
        {
            model.insertRow(1, new Object[]{b.getGuestName(), "", "", "", b.getGuestEmail(), "", ""});
            
        }
        
    }
    
