    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        ArrayList<room> listRoom = new ArrayList<>();
        roomDAO rd = new roomDAO();
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        model.setRowCount(0);
        listRoom = rd.showRooms();
        for(int i = 0; i < listRoom.size(); i++)
        {
            model.addRow(new Object[]{listRoom.get(i).getRoomNo(), listRoom.get(i).getRoomType(), listRoom.get(i).getPeoplePerRoom(), listRoom.get(i).getPricePerDay()});
        }
    }//GEN-LAST:event_formComponentShown

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String roomNo = jTextField3.getText();
        if(roomNo.equals(""))
        {
            JOptionPane.showMessageDialog(this, "Room Number Field Is Required!");
        }
        else
        {
            room rm = new room();
            roomDAO rd = new roomDAO();
            DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
            model.setRowCount(0);
            rm = rd.searchRoom(roomNo);
            model.addRow(new Object[]{rm.getRoomNo(), rm.getRoomType(), rm.getPeoplePerRoom(), rm.getPricePerDay()});
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int index = jTable1.getSelectedRow();
        TableModel model = jTable1.getModel();
        String roomNo = model.getValueAt(index, 0).toString();
        String roomType = model.getValueAt(index, 1).toString();
        String peoplePerRoom = model.getValueAt(index, 2).toString();
        String pricePerDay = model.getValueAt(index, 3).toString();

        int a = JOptionPane.showConfirmDialog(null, "Do you want to update room "+roomNo+"","Select",JOptionPane.YES_NO_OPTION);
        if(a == 0)
        {
            jTextField1.setText(roomNo);
            jComboBox1.setSelectedItem(roomType);
            jComboBox2.setSelectedItem(peoplePerRoom);
            jTextField2.setText(pricePerDay);
        }
    }//GEN-LAST:event_jTable1MouseClicked

