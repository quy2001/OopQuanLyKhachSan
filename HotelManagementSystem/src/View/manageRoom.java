    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String roomNo = jTextField1.getText();
        String roomType = (String)jComboBox1.getSelectedItem();
        String peoplePerRoom = (String)jComboBox2.getSelectedItem();
        String pricePerDay = jTextField2.getText();
        
        if(roomNo.equals("") || roomType.equals("") || peoplePerRoom.equals("") || pricePerDay.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Every Field is Required");
        }
        else
        {
            room rm = new room();
            rm.setRoomType(roomType);
            rm.setPeoplePerRoom(Integer.parseInt(peoplePerRoom));
            rm.setPricePerDay(Integer.parseInt(pricePerDay));
            rm.setRoomNo(roomNo);
            roomDAO rd = new roomDAO();
            
            if(rd.updateRoom(rm))
            {
                JOptionPane.showMessageDialog(this, "Update Successfully!");
                setVisible(false);
                new manageRoom().setVisible(true);
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

